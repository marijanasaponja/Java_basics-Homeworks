package Domaci_10_Zadatak_01;

public class Radnik {
    int id;
    String naziv_radnika;
    int godine_iskustva;
    double plata;

    public Radnik(int id, String naziv_radnika, int godine_iskustva, double plata) {
        this.id = id;
        this.naziv_radnika = naziv_radnika;
        this.godine_iskustva = godine_iskustva;
        this.plata = plata;
    }
}
